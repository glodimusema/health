        ]);

        $idEnteteCons=0;

        $consList = DB::table('tdetailconsultation')
        ->select("tdetailconsultation.id","refEnteteCons")
        ->where([
            ['tdetailconsultation.id',$request->refDetailCons]
        ])
        ->get();
        foreach ($consList as $liste_mvt) {
            $idEnteteCons= $liste_mvt->refEnteteCons;
