    function insert_enteteTriage(Request $request)
    {
       
        $data = tentetetriage::create([
            'refMouvement'       =>  $request->refMouvement,
            'dateTriage'    =>  $request->dateTriage,                      
            'author'       =>  $request->author
        ]);
        return response()->json([
            'data'  =>  "Insertion avec succès!!!",
        ]);
    }


    function update_enteteTriage(Request $request, $id)
    {
        $data = tentetetriage::where('id', $id)->update([
            'refMouvement'       =>  $request->refMouvement,
            'dateTriage'    =>  $request->dateTriage,                      
            'author'       =>  $request->author
