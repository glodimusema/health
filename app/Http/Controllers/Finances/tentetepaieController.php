    function insert_entete(Request $request)
    {
       
        $data = tentetepaiement::create([
            'refMouvement'       =>  $request->refMouvement,
            'dateentetepaie'    =>  $request->dateentetepaie,                           
            'author'       =>  $request->author
        ]);
        return response()->json([
            'data'  =>  "Insertion avec succès!!!",
        ]);
    }


    function update_entete(Request $request, $id)
    {
        $data = tentetepaiement::where('id', $id)->update([
            'refMouvement'       =>  $request->refMouvement,
            'dateentetepaie'    =>  $request->dateentetepaie,                           
            'author'       =>  $request->author
        ]);
        return response()->json([
            'data'  =>  "Modification  avec succès!!!",
        ]);
    }

    function delete_entete($id)
    {
        $data = tentetepaiement::where('id',$id)->delete();
        return response()->json([
            'data'  =>  "suppression avec succès",
        ]);
        
    }
